def build_explanation(num):
    q = questions[num]
    ans = q["answer"]
    base = BASE[num]
    wrong = WRONG.get(num, "")
    extra = EXTRA.get(num, "")
    text = base + wrong + extra

    if len(text) < 120:
        opts = re.findall(r"([A-E])[．.、\s]+(.+?)(?=[A-E][．.、\s]|$)", q["options"])
        wrong_parts = []
        for letter, content in opts:
            if letter != ans:
                content = content.strip().rstrip("。")
                if len(content) > 16:
                    content = content[:16] + "…"
                wrong_parts.append(f"{letter}项（{content}）与题干不符")
        if wrong_parts:
            text = text.rstrip("。") + "。" + "；".join(wrong_parts) + "。"
    return text

