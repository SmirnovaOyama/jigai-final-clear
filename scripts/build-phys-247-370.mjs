function getOptText(q, letter) {
  const re = new RegExp(`${letter}．\\s*([\\s\\S]*?)(?=\\s*[A-Z]．|$)`);
  const m = q.options.match(re);
  return m ? m[1].trim().replace(/\s+/g, '') : '';
}

function buildExplanation(q) {
  const c = core[q.number];
  const w = wrong[q.number];
  const letters = ['A', 'B', 'C', 'D', 'E'];
  const optParts = letters.map((l) => {
    const label = `${l}项（${getOptText(q, l)}）`;
    if (l === q.answer) return `${label}正确，符合题干要求`;
    return `${label}错误，${w[l]}`;
  });
  return `${c} 选项分析：${optParts.join('；')}。`;
}

