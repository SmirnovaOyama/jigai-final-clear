function ensureLength(kp) {
  if (kp.length >= 60) return kp.length > 130 ? kp.slice(0, 130) : kp;
  const suffix = '，理解生理机制与正常参考值，掌握核心概念。';
  let s = kp.replace(/[。．]$/, '') + suffix;
  if (s.length > 130) s = s.slice(0, 130);
  return s;
}

const dir = path.resolve('scripts/patches');

for (const [start, end, name] of [[1, 93, 'kp-physiology-1-93.json'], [94, 185, 'kp-physiology-94-185.json']]) {
  const updates = [];
  const warnings = [];
  for (let n = start; n <= end; n++) {
    const kp = ensureLength(KP[n]);
    if (!kp) throw new Error(`Missing KP for ${n}`);
    if (kp.length < 60 || kp.length > 130) warnings.push(`Q${n} length ${kp.length}`);
    if (/["']/.test(kp)) throw new Error(`ASCII quotes in Q${n}`);
    updates.push({ number: n, knowledgePoint: kp });
  }
  if (warnings.length) {
    console.warn(`${name} length warnings:\n` + warnings.join('\n'));
  }
  const out = { file: FILE, updates };
  fs.writeFileSync(path.join(dir, name), JSON.stringify(out, null, 2) + '\n');
  console.log(`Wrote ${name}: ${updates.length} entries`);
}
